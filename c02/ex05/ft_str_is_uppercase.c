int	ft_str_is_uppercase(char *str)
{
	char	j;
	int		i;

	i = 0;
	while (str[i])
	{
		j = str[i];
		if (!(j >= 'A' && j <= 'Z'))
			return (0);
		i++;
	}
	return (1);
}
